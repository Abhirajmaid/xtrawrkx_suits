// Helper functions
export const getLeadsByStatus = (status) => {
  if (status === "all") return leadsData;
  return leadsData.filter(lead => lead.status === status);
};

export const getLeadStats = (leads = leadsData) => {
  const stats = {
    total: leads.length,
    new: leads.filter(lead => lead.status === "new").length,
    contacted: leads.filter(lead => lead.status === "contacted").length,
    qualified: leads.filter(lead => lead.status === "qualified").length,
    lost: leads.filter(lead => lead.status === "lost").length,
  };
  return stats;
};

export const searchLeads = (query, leads = leadsData) => {
  if (!query.trim()) return leads;
  
  const lowercaseQuery = query.toLowerCase();
  return leads.filter(lead => 
    lead.name.toLowerCase().includes(lowercaseQuery) ||
    lead.company.toLowerCase().includes(lowercaseQuery) ||
    lead.email.toLowerCase().includes(lowercaseQuery) ||
    lead.phone.includes(query) ||
    lead.industry.toLowerCase().includes(lowercaseQuery)
  );
};

export const filterLeads = (filters, leads = leadsData) => {
  let filteredLeads = [...leads];
  
  if (filters.status && filters.status !== "all") {
    filteredLeads = filteredLeads.filter(lead => lead.status === filters.status);
  }
  
  if (filters.source) {
    filteredLeads = filteredLeads.filter(lead => lead.source === filters.source);
  }
  
  if (filters.assignee) {
    filteredLeads = filteredLeads.filter(lead => lead.assignee === filters.assignee);
  }
  
  if (filters.industry) {
    filteredLeads = filteredLeads.filter(lead => lead.industry === filters.industry);
  }
  
  if (filters.minValue) {
    filteredLeads = filteredLeads.filter(lead => lead.value >= filters.minValue);
  }
  
  if (filters.maxValue) {
    filteredLeads = filteredLeads.filter(lead => lead.value <= filters.maxValue);
  }
  
  return filteredLeads;
};

